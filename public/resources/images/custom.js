// Because of the routing to captcha when sending a form and hiding/showing with alpine, show a 'message sent' box if URL parameter includes ?contact_posted=true
// This is used on the product page and build a box page, since both hide their forms on load in either tabs/accordions or modals

function getParameterByName(name, url) {
  if (!url) url = window.location.href;
  name = name.replace(/[\[\]]/g, "\\$&");
  const regex = new RegExp("[?&]" + name + "(=([^&#]*)|&|#|$)"),
      results = regex.exec(url);
  if (!results) return null;
  if (!results[2]) return '';
  return decodeURIComponent(results[2].replace(/\+/g, " "));
}

const dynamicContent = getParameterByName('contact_posted');
const messageSent = document.getElementById('form-sent-msg');

if (dynamicContent == 'true') {
  console.log('true');
  messageSent.style.display = 'block';
}
